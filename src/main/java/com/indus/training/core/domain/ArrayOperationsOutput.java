package com.indus.training.core.domain;

/**
 * Represents the output for array operations involving two 2x2 matrices.
 */
public class ArrayOperationsOutput {

	/**
	 * The first 2x2 matrix used in the array operations.
	 */
	public double[][] param1 = new double[2][2];
	/**
	 * The second 2x2 matrix used in the array operations.
	 */
	public double[][] param2 = new double[2][2];
	/**
	 * The 2x2 matrix that stores the result of the array operations.
	 */
	public double[][] result = new double[2][2];

}
